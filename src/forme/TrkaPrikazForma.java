     void dodajTrku(UcesceForma aThis, Vozac v) {
    try {
        ModelTabeleUcesce mtu=(ModelTabeleUcesce) tabelaUcesca.getModel();
        mtu.dodaj(v);
        JOptionPane.showMessageDialog(aThis, "Vozac je dodat!");
    } catch (IOException ex) {
        Logger.getLogger(TrkaPrikazForma.class.getName()).log(Level.SEVERE, null, ex);
    }
    }

