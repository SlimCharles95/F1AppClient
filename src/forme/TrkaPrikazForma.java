    private void btnKreirajIzveštajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKreirajIzveštajActionPerformed
 int red=tabelaUcesca.getSelectedRow();
        if(red!=-1){
            ModelTabeleUcesce mtu=(ModelTabeleUcesce) tabelaUcesca.getModel();
            Ucesce u=mtu.getRed(red);
IzvestajIncidentaForma iif=new IzvestajIncidentaForma(this, true);
iif.setUcesce(u);
iif.pack();
iif.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(this, "Niste izabrali učešće!");
            return;
        }
    }//GEN-LAST:event_btnKreirajIzveštajActionPerformed

